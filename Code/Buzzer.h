/**@brief
	*This function will initialize the GPIO for the Buzzer
	*/
void buzzer_init(void);
/**@brief
	*This function will write high signal to the GPIO and will turn on the buzzer
	*/
void buzzer_on(void);
/**@brief
	*This function will write low signal to the GPIO and will turn off the buzzer
	*/
void buzzer_off(void);